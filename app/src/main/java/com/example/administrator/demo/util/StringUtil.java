    /**
     * 将一个InputStream流转换成字符串
     *
     * @param is
     * @return
     */
    public static String toConvertString(InputStream is) {
        StringBuilder res = new StringBuilder();
        InputStreamReader isr = new InputStreamReader(is);
        BufferedReader read = new BufferedReader(isr);
        try {
            String line;
            line = read.readLine();
            while (line != null) {
                res.append(line);
                line = read.readLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (null != isr) {
                    isr.close();
                    isr.close();
                }
                if (null != read) {
                    read.close();
                    read = null;
                }
                if (null != is) {
                    is.close();
                    is = null;
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return res.toString();
    }

    /*********有时候，将一个ArrayList转换成字符串式原始编码，保存在文件中，等使用时在取出，下方两个函数实现******/
    /**
     * 将list数据解析成字符串，字符串为原始编码,
     *
     * @param SceneList
     * @return
     * @throws IOException
     */
    private static String SurveyList2String(ArrayList SceneList) throws IOException {
        if (SceneList == null)
            return null;
        Log.e("CacheUitls", "sceneList不为空");
        // 实例化一个ByteArrayOutputStream对象，用来装载压缩后的字节文件。
        ByteArrayOutputStream byteArrayOutputStream = new ByteArrayOutputStream();
        // 然后将得到的字符数据装载到ObjectOutputStream
        ObjectOutputStream objectOutputStream = new ObjectOutputStream(byteArrayOutputStream);
        // writeObject 方法负责写入特定类的对象的状态，以便相应的 readObject 方法可以还原它
        objectOutputStream.writeObject(SceneList);
        // 最后，用Base64.encode将字节文件转换成Base64编码保存在String中
        String SceneListString = new String(Base64.encode(
                byteArrayOutputStream.toByteArray(), Base64.DEFAULT));

        // 关闭objectOutputStream
        objectOutputStream.flush();
        objectOutputStream.close();
        return SceneListString;
    }

    /**
     * 将原始编码的字符串解析成SurveyList
     *
     * @param SceneListString
     * @return
     * @throws IOException
     * @throws ClassNotFoundException
     */
    @SuppressWarnings("unchecked")
    private static ArrayList String2SurveyList(String SceneListString) throws IOException,
            ClassNotFoundException {
        if (SceneListString == null || isEmpty(SceneListString))
            return null;
        //将base64解码
        byte[] mobileBytes = Base64.decode(SceneListString.getBytes(),
                Base64.DEFAULT);
        ByteArrayInputStream byteArrayInputStream = new ByteArrayInputStream(
                mobileBytes);
        ObjectInputStream objectInputStream = new ObjectInputStream(byteArrayInputStream);
        ArrayList SceneList = (ArrayList) objectInputStream.readObject();
        if (SceneList == null) {
            return null;
        }

        objectInputStream.close();
        return SceneList;
    }

